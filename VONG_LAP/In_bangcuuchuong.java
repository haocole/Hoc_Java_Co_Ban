package VONG_LAP;

public class In_bangcuuchuong {
    public static void main(String[] args) {
        System.out.println("Bảng cửu chương 5:");
        for (int i = 1; i <= 10; i++) {
            // In theo định dạng: 5 x i = kết quả
            System.out.println("5 x " + i + " = " + (5 * i));
        }
    }
    
}
